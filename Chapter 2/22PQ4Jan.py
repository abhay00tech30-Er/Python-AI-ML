"""a. Implicit Conversion (Automatic)
Python automatically converts smaller data types to larger ones to prevent data loss."""
x = 5      # int
y = 2.5    # float
z = x + y  # Python converts int â†’ float
print(z)   # 7.5
print(type(z))
print(id(z))

"""b. Explicit Conversion (Manual)
You can manually convert data types using built-in functions"""
x = 5
y = float(x)  # Convert int to float
print(y)    
print(type(y))
print(id(y))

