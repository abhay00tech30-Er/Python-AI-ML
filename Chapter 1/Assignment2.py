#Question : Take the Diameter as input and calculate the area of a circle
Dia = float(input("Enter The Diameter Of The Circle : "))
Radius = Dia/2 
Area =  3.14 * (Radius**2) 
print("Diameter of The Circle :  " , Dia )
print("Radius of The Circle : ", Radius)
print("The Area Of The Circle : " , Area)       